#include <unistd.h>

void	ft_putchar(int x, int y, int i, int o )
{
	char	letra;

	letra = ' ';
	if ((o == 0 && i == 0) || (o == x - 1 && i == 0))
	{
		letra = 'A';
	}
	else if ((o == 0 && i == y - 1) || (o == x - 1 && i == y - 1))
	{
		letra = 'C';
	}
	else if ((o != 0 && o != x - 1 && (i == 0 || i == y - 1))
		|| ((o == 0 || o == x - 1) && i != 0 && i != y - 1))
	{
		letra = 'B';
	}
	write(1, &letra, 1);
}
