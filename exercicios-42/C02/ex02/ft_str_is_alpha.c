int	ft_str_is_alpha(char *str)
{
	char	c;

	while (str[0] != '\0')
	{
		c = str[0];
		if (!(c >= 'a' && c <= 'z') && !(c >= 'A' && c <= 'Z'))
			return (0);
		str++;
	}
	return (1);
}
